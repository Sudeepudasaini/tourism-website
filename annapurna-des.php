Annapurna I is the world’s 10th highest peak. Although climbers had reached 28,150 feet (8,580 meters) on Mount Everest by 1924, Annapurna I became famous in 1950 as the first peak above 26,000 feet (8,000 meters) to be ascended to the summit. The feat was achieved by a French expedition led by Maurice Herzog, who with Louis Lachenal reached the top on June 3. Annapurna IV was climbed on May 30, 1955, by a German team of Harald Biller, Heinz Steinmetz, and Jürgen Wellenkamp; and Annapurna II on May 17, 1960, by the British climbers Richard Grant and Chris Bonington and the Sherpa Ang Nyima in an expedition led by James O.M. Roberts. In 1970 an all-women Japanese climbing team scaled Annapurna III.Annapurna (/ˌænəˈpʊərnəˌ -ˈpɜːr-/;[4][5] Nepali: अन्नपूर्ण) is a mountain situated in the Annapurna mountain range of Gandaki Province, north-central Nepal. It is the 10th highest mountain in the world at 8,091 metres (26,545 ft) above sea level and is well known for the difficulty and danger involved in its ascent.

Maurice Herzog led a French expedition to its summit through the north face in 1950, making it the first eight-thousander to be successfully climbed.[6] The entire massif and surrounding area are protected within the 7,629-square-kilometre (2,946 sq mi) Annapurna Conservation Area, the first and largest conservation area in Nepal. The Annapurna Conservation Area is home to several world-class treks, including Annapurna Sanctuary and Annapurna Circuit.



