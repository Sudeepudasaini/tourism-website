        <p style="text-align: justify;"> 

        Pokhara’s tranquil beauty has been the subject of inspiration for many travel writers. Its pristine air, spectacular backdrop of snowy peaks, blue lakes and surrounding greenery make it ‘the jewel in the Himalaya’, a place of remarkable natural disposition. With the magnificent Annapurna range forming the backdrop and the serenity of the Cluster of 9 Lakes with three major ones - Phewa, Rupa and Begnas – Pokhara is a great destination for a weekend getaway as well as a long relaxing holiday. Pokhara Valley, gateway to the Annapurna region where many a trekker finds his Shangri-la, sits high on the list of ‘must visit’ places in Nepal.

Pokhara once lay on the important trade route between India and Tibet. To this day, mule trains set up camps on the city outskirts, bringing goods from remote Himalayan regions including Mustang. Gurungs and Magars, who have earned world-wide fame as fierce Gurkha warriors, are predominant here. Thakalis, indigenous of the Thak Khola region of Mustang, are known for their entrepreneurship and run tea houses along the trek routes in the Annapurna region. The Pokhara is best known for the stunning view of the Annapurna range. It is perhaps one of the few places on earth from where mountains above 6,000 m can be seen unobstructed from an altitude of 800 m within the distance of 28 km. Pokhara was envisioned as a commercial center by the King of Kaski in the mid 18th century A.D.[17] when Newars of Bhaktapur migrated to Pokhara, upon being invited by the king, and settled near main business locations such as Bindhyabasini temple, Nalakomukh and Bhairab Tole. Most of Pokhara, at the time, was largely inhabited by Khas[18] (Brahmin, Chhetri, Thakuri and Dalits), Gurungs, and Magars.[19][20] At present, the Khas, Gurung (Tamu) and Magar form the dominant community of Pokhara. There is also a sizeable Newari population in the city.[21] Batulechaur in the far north of Pokhara is home to the Gandharvas or Gaaineys (the tribe of the musicians).[22]</p>
       
