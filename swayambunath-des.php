        A golden spire crowning a conical wooded hill, Swayambhunath Stupa is the most ancient and enigmatic of all the holy shrines in Kathmandu valley. Its lofty white dome and glittering golden spire are visible for many miles and from all sides of the valley. Historical records found on a stone inscription give evidence that the stupa was already an important Buddhist pilgrimage destination by the 5th century AD. Its origins however, date to a much earlier time, long before the arrival of Buddhism into the valley. A collection of legends about the site, the 15th century Swayambhu Purana, tells of a miraculous lotus, planted by a past Buddha, which blossomed from the lake that once covered Kathmandu valley. The lotus mysteriously radiated a brilliant light, and the name of the place came to be Swayambhu, meaning 'Self-Created or Self-Existent'. Saints, sages and divinities traveled to the lake to venerate this miraculous light for its power in granting enlightenment. During this time, the Bodhisatva Manjushri was meditating at the sacred mountain of Wu Tai Shan and had a vision of the dazzling Swayambhu light. Manjushri flew across the mountains of China and Tibet upon his blue lion to worship the lotus. Deeply impressed by the power of the radiant light, Manjushri felt that if the water were drained out of the lake Swayambhu would become more easily accessible to human pilgrims.Manjusri had a vision of the Lotus at Swayambhu and traveled there to worship it. Seeing that the valley could be a good settlement, and to make the site more accessible to human pilgrims, he cut a gorge at Chovar. The water drained out of the lake, leaving the valley in which Kathmandu now lies. The Lotus was transformed into a hill and the flower became the stupa.
